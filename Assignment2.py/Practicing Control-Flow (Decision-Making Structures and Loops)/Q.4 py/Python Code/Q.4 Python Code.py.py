# Using different control-flow structures

# If-else statement
marks = 75
if marks >= 50:
    print("Pass")
else:
    print("Fail")

# For loop with condition
for i in range(1, 6):
    if i % 2 == 0:
        print(i, "is even")
    else:
        print(i, "is odd")

# While loop
count = 3
while count > 0:
    print("Countdown:", count)
    count -= 1










